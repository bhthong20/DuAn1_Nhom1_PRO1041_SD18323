    private void loadCTSPTT() {
        List<ChiTietSanPham> listCTSP = hdsi.getListCTSPTT();
        model = (DefaultTableModel) tblCTSP.getModel();
        model.setRowCount(0);
        for (ChiTietSanPham x : listCTSP) {
            Object row[] = new Object[]{
                x.getMaCTSP(), x.getSanPham().getTenSP(), x.getMauSac().getTenMS(), x.getChatLieu().getTenCL(), x.getKichThuoc().getTenKT(), x.getDongSP().getTenDongSP(), x.getNsx().getTenNhaSanXuat(), formatter.format(x.getGiaBan()), x.getSoLuongTon(), formatter.format(x.getKhuyenMai())
            };
            model.addRow(row);
        }
    }

    private void loadHDCTTT() {
        List<HoaDonChiTiet> listHDCT = hdsi.getListHDCTTT(Long.parseLong(lblMHD.getText()));
        model = (DefaultTableModel) tblHDCT.getModel();
        model.setRowCount(0);
        for (HoaDonChiTiet x : listHDCT) {
            Object row[] = new Object[]{
                x.getMaHDCTTT(), x.getMaCTSPCT().getMaCTSP(), x.getTenSP(), formatter.format(x.getDonGia()), x.getSoLuong()
            };
            model.addRow(row);
        }
    }

    public void xuatHoaDon(int maHD) {
        try {
            Hashtable map = new Hashtable();
            JasperReport report = JasperCompileManager.compileReport("src/gui/newReport.jrxml");
            map.put("MaHoaDon", maHD);
            JasperPrint p = JasperFillManager.fillReport(report, map, DBConnection.getConnection());
            JasperViewer.viewReport(p, false);
            JasperExportManager.exportReportToPdfFile(p, "test.pdf");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

