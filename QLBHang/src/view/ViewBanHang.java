    private void tblCTSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCTSPMouseClicked
        // TODO add your handling code here:
        try {
            Integer row1 = tblCTSP.getSelectedRow();
            int soluongTon = (int) tblCTSP.getValueAt(row1, 8);
            if (lblMHD.getText().isEmpty() || Integer.parseInt(lblMHD.getText()) == 0) {
                Icon icon = new ImageIcon(getClass().getResource("/iImage/Exitmini.png"));
                JOptionPane.showMessageDialog(this, "Chưa có hóa đơn!", "Lỗi", JOptionPane.INFORMATION_MESSAGE, icon);
                return;
            }
            if (soluongTon < 0) {
                Icon icon = new ImageIcon(getClass().getResource("/iImage/Exitmini.png"));
                JOptionPane.showMessageDialog(this, "Hết hàng !", "Lỗi", JOptionPane.INFORMATION_MESSAGE, icon);
                return;
            }
            Integer row = tblCTSP.getSelectedRow();
            String maCTSP = (String) tblCTSP.getValueAt(row, 0);
            ChiTietSanPham maHDCT = new ChiTietSanPham(maCTSP);
            HoaDon don = new HoaDon(Long.parseLong(lblMHD.getText()));
            String string = (String) tblCTSP.getValueAt(row, 7);
            String soLuongInput = JOptionPane.showInputDialog("Nhập số lượng sản phẩm" + " ");
            if (isNumeric(soLuongInput)) {
                Integer soLuong1 = Integer.parseInt(soLuongInput);
                String maCTSP1 = (String) tblCTSP.getValueAt(row, 0);
                hdsi.updateCTSP(maCTSP1, soLuong1);
                HoaDonChiTiet chiTiet = new HoaDonChiTiet();
                chiTiet.setMaHDCT(don);
                chiTiet.setMaCTSPCT(maHDCT);
                chiTiet.setDonGia(convertedToNumbers(string));
                chiTiet.setSoLuong(soLuong1);
                hdsi.addHDCTTT(chiTiet);
                int khuyenmai = hdsi.getKhuyenMai(Long.parseLong(lblMHD.getText()));
                int tongtientt = hdsi.getTongTien(Long.parseLong(lblMHD.getText()));
                lblTongTien.setText(formatter.format(tongtientt));
                lblKhuyenMai.setText(formatter.format(khuyenmai));
                int tien = tongtientt - khuyenmai;
                lblKhuyenMai1.setText(formatter.format(tien));
                loadHDCTTT();
                loadCTSPTT();
            } else {
                Icon icon = new ImageIcon(getClass().getResource("/iImage/Exitmini.png"));
                JOptionPane.showMessageDialog(this, "Nhập đúng số lượng", "Lỗi", JOptionPane.INFORMATION_MESSAGE, icon);
            }
        } catch (Exception e) {
            System.out.println(e);
            Icon icon = new ImageIcon(getClass().getResource("/iImage/Exitmini.png"));
            JOptionPane.showMessageDialog(this, "Thất bại", "Lỗi", JOptionPane.INFORMATION_MESSAGE, icon);
        }
    }//GEN-LAST:event_tblCTSPMouseClicked

